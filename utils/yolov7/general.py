def strip_optimizer(f='best.pt', s=''):  # from yolov7.general import *; strip_optimizer()
    # Strip optimizer from 'f' to finalize training, optionally save as 's'
    x = torch.load(f, map_location=torch.device('cpu'))
    if x.get('ema'):
        x['model'] = x['ema']  # replace model with ema
    for k in 'optimizer', 'training_results', 'wandb_id', 'ema', 'updates':  # keys
        x[k] = None
    x['epoch'] = -1
    x['model'].half()  # to FP16
    for p in x['model'].parameters():
        p.requires_grad = False
    torch.save(x, s or f)
    mb = os.path.getsize(s or f) / 1E6  # filesize
    print(f"Optimizer stripped from {f},{(' saved as %s,' % s) if s else ''} {mb:.1f}MB")


def apply_classifier(x, model, img, im0):
    # applies a second stage classifier to yolo outputs
    im0 = [im0] if isinstance(im0, np.ndarray) else im0
    for i, d in enumerate(x):  # per image
        if d is not None and len(d):
            d = d.clone()

            # Reshape and pad cutouts
            b = xyxy2xywh(d[:, :4])  # boxes
            b[:, 2:] = b[:, 2:].max(1)[0].unsqueeze(1)  # rectangle to square
            b[:, 2:] = b[:, 2:] * 1.3 + 30  # pad
            d[:, :4] = xywh2xyxy(b).long()

            # Rescale boxes from img_size to im0 size
            scale_coords(img.shape[2:], d[:, :4], im0[i].shape)

            # Classes
            pred_cls1 = d[:, 5].long()
            ims = []
            for j, a in enumerate(d):  # per item
                cutout = im0[i][int(a[1]):int(a[3]), int(a[0]):int(a[2])]
                im = cv2.resize(cutout, (224, 224))  # BGR
                # cv2.imwrite('test%i.jpg' % j, cutout)

                im = im[:, :, ::-1].transpose(2, 0, 1)  # BGR to RGB, to 3x416x416
                im = np.ascontiguousarray(im, dtype=np.float32)  # uint8 to float32
                im /= 255.0  # 0 - 255 to 0.0 - 1.0
                ims.append(im)

            pred_cls2 = model(torch.Tensor(ims).to(d.device)).argmax(1)  # classifier prediction
            x[i] = x[i][pred_cls1 == pred_cls2]  # retain matching class detections

    return x


def transform_to_yolov7_targets(targets, logits=False):
    # input targets is list of array n_img, n_boxes, [x1, y1, x2, y2 , cls_id/logits, box_score (for earl only), weights (for bdc)]
    # output targets for yolov7 should be [img_idx, (weights), cls_id/logits, x-center, y-center, w, h] (normalized)
    final_targets = []
    for i, target in enumerate(targets):
        temp = torch.zeros((target.shape[0], target.shape[1] + 1))
        temp[:, 0] = i  # add target image index for build_targets()
        temp[:, -4:] = torch.from_numpy(xyxy2xywh(target[:, :4]))
        if logits:
            temp[:, 2:-4] = torch.from_numpy(target[:, 4:-1])
            temp[:, 1] = torch.from_numpy(target[:, -1])  # weight
        else:
            temp[:, 1] = torch.from_numpy(target[:, 4])  # class id
            if target.shape[1] == 6:  # earl
                temp[:, 2] = torch.from_numpy(target[:, 2])
        final_targets.append(temp)
    return torch.cat(final_targets, 0)
